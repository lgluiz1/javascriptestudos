//JavaScript tem seis tipos de dados primitivos:

//Number: representa valores numéricos, seja inteiros ou de ponto flutuante.
//String: representa uma sequência de caracteres, como um nome ou um endereço de e-mail.
//Boolean: representa um valor verdadeiro ou falso.
//Null: representa um valor nulo intencionalmente atribuído a uma variável.
//Undefined: representa um valor não atribuído ou não definido.
//Symbol: representa um valor único e imutável que pode ser usado como identificador de propriedade de objeto.
//Geralmente, esses tipos primitivos são usados para armazenar valores simples e não objetos mais complexos.

//exemplos 
let nome = 'luiz'; // String literal
let idade = 28; // Number literal
let estaAprovado = true; //Boolean 
let sobrenome ; //undefined
let corSelecionado = null // null - Redefinir um Valor

